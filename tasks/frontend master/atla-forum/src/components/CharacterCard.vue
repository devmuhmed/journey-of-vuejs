<script>
export default {
    props: {
        character: {
            type: Object,
            required: true
        }
    },
    emits: ['favorite'],
  methods: {
    favoriteCharacter() {
      this.$emit('favorite', this.character)
    }
  }
}
</script>

<template>
  <p>
    {{ character.name }}
  </p>
  <button @click="favoriteCharacter">‚≠ê Favorite</button>
</template>
